"use client"

import * as React from "react"
import { Moon, Sun } from "lucide-react"
import { useTheme } from "next-themes"
import { Button } from "@/components/ui/button"

export function ModeToggle() {
  const { theme, setTheme } = useTheme()

  return (
    <Button
      variant="outline"
      size="icon"
      onClick={() =>{
        setTheme(theme === "light" ? "dark" : "light")
      } }
      className=" rounded-xl border dark:border-neutral-600 dark:hover:bg-neutral-800 bg-white border-neutral-300 hover:bg-gray-200 transition-colors dark:bg-neutral-900 dark:text-neutral-400 dark:hover:bg-neutral-800/50"
    >
      <Sun className="h-[1.2rem] w-[1.2rem] rotate-0 scale-100 transition-all dark:-rotate-90 dark:scale-0" />
      <Moon className="absolute h-[1.2rem] w-[1.2rem] rotate-90 scale-0 transition-all dark:rotate-0 dark:scale-100" />
      <span className="sr-only">Toggle theme</span>
    </Button>
  )
}